/**
 * Fleet Markdown reporter — for GitHub issue/Slack posting.
 * @param {import('../fleet.js').FleetResult} fleet
 */
export function reportFleetMarkdown(fleet) {
  const now = new Date().toISOString().split('T')[0];
  const lines = [];

  lines.push(`# ⚗️ Vibe Audit Fleet Report — ${now}`);
  lines.push('');
  lines.push(`| Metric | Value |`);
  lines.push(`|--------|-------|`);
  lines.push(`| Repos scanned | ${fleet.scannedRepos} |`);
  lines.push(`| Failed | ${fleet.failedRepos} |`);
  lines.push(`| Critical | ${fleet.totalCritical} |`);
  lines.push(`| Warnings | ${fleet.totalWarning} |`);
  lines.push(`| Info | ${fleet.totalInfo} |`);
  lines.push(`| Total findings | ${fleet.totalFindings} |`);
  lines.push(`| Duration | ${(fleet.durationMs / 1000).toFixed(1)}s |`);
  lines.push('');

  // Grade distribution
  const grades = { A: 0, B: 0, C: 0, D: 0, F: 0 };
  for (const r of fleet.repos) {
    if (r.grade in grades) grades[r.grade]++;
  }
  lines.push('## Grade Distribution');
  lines.push('');
  lines.push(`| Grade | Count |`);
  lines.push(`|-------|-------|`);
  for (const [g, n] of Object.entries(grades)) {
    if (n > 0) lines.push(`| ${g} | ${n} |`);
  }
  lines.push('');

  // Repos with criticals
  const critRepos = fleet.repos.filter(r => r.critical > 0);
  if (critRepos.length > 0) {
    lines.push('## 🔴 Repos with Critical Issues');
    lines.push('');
    for (const r of critRepos.sort((a, b) => b.critical - a.critical)) {
      lines.push(`### \`${r.repo}\` — ${r.critical} critical`);
      lines.push('');
      for (const f of r.findings.filter(f => f.severity === 'critical').slice(0, 10)) {
        lines.push(`- **${f.message}** (\`${f.file}${f.line ? ':' + f.line : ''}\`)${f.cweId ? ' `' + f.cweId + '`' : ''}`);
      }
      if (r.critical > 10) lines.push(`- _...and ${r.critical - 10} more_`);
      lines.push('');
    }
  }

  // Full table
  lines.push('## All Repos');
  lines.push('');
  lines.push('| Repo | Grade | Critical | Warnings | Info | Files |');
  lines.push('|------|-------|----------|----------|------|-------|');

  const sorted = [...fleet.repos].sort((a, b) => {
    const go = { F: 0, D: 1, C: 2, B: 3, A: 4, '?': 5 };
    return (go[a.grade] ?? 9) - (go[b.grade] ?? 9);
  });

  for (const r of sorted) {
    if (r.error) {
      lines.push(`| ${r.repo} | ERR | — | — | — | _${r.error.slice(0, 40)}_ |`);
    } else {
      lines.push(`| ${r.repo} | ${r.grade} | ${r.critical} | ${r.warning} | ${r.info} | ${r.filesScanned} |`);
    }
  }

  lines.push('');
  lines.push(`---`);
  lines.push(`_Generated by [Vibe Audit](https://github.com/jackdog668/vibeaudit) fleet scanner_`);

  console.log(lines.join('\n'));
}
